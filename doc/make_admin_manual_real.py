# ── Helpers ───────────────────────────────────────────────────────────────────
def run(para, text, bold=False, size=14, color=None, italic=False):
    r = para.add_run(text)
    r.font.name = FONT
    r.font.bold = bold
    r.font.size = Pt(size)
    r.font.italic = italic
    r.element.rPr.rFonts.set(qn('w:eastAsia'), FONT)
    if color:
        r.font.color.rgb = color
    return r

def para(text="", bold=False, size=14, color=None,
         align=WD_ALIGN_PARAGRAPH.LEFT, sb=0, sa=6, italic=False):
    p = doc.add_paragraph()
    p.alignment = align
    p.paragraph_format.space_before = Pt(sb)
    p.paragraph_format.space_after  = Pt(sa)
    if text:
        run(p, text, bold=bold, size=size, color=color, italic=italic)
    return p

def heading(text, level=1):
    h = doc.add_heading(text, level=level)
    h.paragraph_format.space_before = Pt(14 if level == 1 else 10)
    h.paragraph_format.space_after  = Pt(6)
    for r2 in h.runs:
        r2.font.name = FONT
        r2.element.rPr.rFonts.set(qn('w:eastAsia'), FONT)

def bullet(text, indent=0):
    p = doc.add_paragraph(style='List Bullet')
    p.paragraph_format.left_indent = Cm(0.5 + indent * 0.5)
    p.paragraph_format.space_after = Pt(3)
    run(p, text, size=14)

def numbered(text, indent=0):
    p = doc.add_paragraph(style='List Number')
    p.paragraph_format.left_indent = Cm(0.5 + indent * 0.5)
    p.paragraph_format.space_after = Pt(3)
    run(p, text, size=14)

def add_image(fname, width_cm=14, caption=None):
    path = f"{SS}/{fname}"
    if not os.path.exists(path):
        para(f"[ภาพ: {fname} — ไม่พบไฟล์]", italic=True, color=GRAY)
        return
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after  = Pt(4)
    r = p.add_run()
    r.add_picture(path, width=Cm(width_cm))
    if caption:
        cp = doc.add_paragraph()
        cp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        cp.paragraph_format.space_after = Pt(10)
        run(cp, caption, size=11, color=GRAY, italic=True)

def simple_table(headers, rows, col_widths=None):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = 'Table Grid'
    t.alignment = WD_TABLE_ALIGNMENT.LEFT
    hrow = t.rows[0]
    for i, h in enumerate(headers):
        cell = hrow.cells[i]
        _shade(cell, '1A1F2E')
        cp = cell.paragraphs[0]
        cp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run(cp, h, bold=True, size=12, color=RGBColor(0xFF, 0xFF, 0xFF))
    for ri, row_data in enumerate(rows):
        row = t.rows[ri+1]
        fill = 'F8F9FA' if ri % 2 == 0 else 'FFFFFF'
        for ci, cell_text in enumerate(row_data):
            cell = row.cells[ci]
            _shade(cell, fill)
            cp = cell.paragraphs[0]
            run(cp, str(cell_text), size=12)
    if col_widths:
        n_cols = len(t.columns)
        for ci, w in enumerate(col_widths):
            if ci >= n_cols:
                break
            for row in t.rows:
                row.cells[ci].width = Cm(w)
    doc.add_paragraph().paragraph_format.space_after = Pt(6)
    return t

def _shade(cell, fill_hex):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), fill_hex)
    tcPr.append(shd)

def note_box(text):
    t = doc.add_table(rows=1, cols=1)
    t.alignment = WD_TABLE_ALIGNMENT.LEFT
    cell = t.cell(0, 0)
    _shade(cell, 'FFF3CD')
    cp = cell.paragraphs[0]
    cp.paragraph_format.space_before = Pt(4)
    cp.paragraph_format.space_after  = Pt(4)
    run(cp, "⚠ ", bold=True, size=13, color=RGBColor(0x85, 0x64, 0x04))
    run(cp, text, size=13, color=RGBColor(0x85, 0x64, 0x04))
    doc.add_paragraph().paragraph_format.space_after = Pt(4)

def info_box(text):
    t = doc.add_table(rows=1, cols=1)
    t.alignment = WD_TABLE_ALIGNMENT.LEFT
    cell = t.cell(0, 0)
    _shade(cell, 'E6F4F4')
    cp = cell.paragraphs[0]
    cp.paragraph_format.space_before = Pt(4)
    cp.paragraph_format.space_after  = Pt(4)
    run(cp, "ℹ ", bold=True, size=13, color=TEAL)
    run(cp, text, size=13, color=DARK)
    doc.add_paragraph().paragraph_format.space_after = Pt(4)

